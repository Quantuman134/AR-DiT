def test_resumable_sampler_skips_correct_number(tmp_path: Path) -> None:
    """ResumableDistributedSampler advances by ``start_step * bs`` samples."""
    import train as train_module

    dataset = list(range(20))               # 20-element dummy dataset

    sampler = train_module.ResumableDistributedSampler(
        dataset, num_replicas=1, rank=0, shuffle=False, seed=0, drop_last=True,
    )

    # A fresh sampler yields all 20 indices in order.
    fresh = list(iter(sampler))
    assert fresh == list(range(20)), fresh

    # After set_start_step(2, batch_size=4) → skip 2*4=8 samples.
    sampler.set_start_step(start_step=2, batch_size_per_rank=4)
    resumed = list(iter(sampler))
    assert resumed == list(range(8, 20)), resumed


def test_build_lr_scheduler_warmup(tmp_path: Path) -> None:
    """Linear warmup ramps 0 → 1 by warmup_steps, then holds at 1 for constant schedule."""
    import train as train_module
    from configs.schema import OptimConfig

    optim_cfg = OptimConfig(
        name="adamw", lr=1.0, betas=(0.9, 0.999),
        weight_decay=0.0, grad_clip=None,
        warmup_steps=4, lr_schedule="constant",
    )
    dummy = torch.nn.Linear(1, 1)
    opt = torch.optim.AdamW(dummy.parameters(), lr=optim_cfg.lr)
    sched = train_module.build_lr_scheduler(optim_cfg, total_steps=100, optimizer=opt)

    lrs = []
    for _ in range(6):
        opt.step()
        lrs.append(sched.get_last_lr()[0])
        sched.step()

    # LambdaLR calls lr_lambda(step) where step is the number of
    # sched.step() calls made so far — starting at 0.  With warmup=4
    # the ramp is 0/4, 1/4, 2/4, 3/4, 1.0, 1.0, ...
    assert lrs[0] == pytest.approx(0.0)
    assert lrs[1] == pytest.approx(0.25)
    assert lrs[2] == pytest.approx(0.5)
    assert lrs[3] == pytest.approx(0.75)
    assert lrs[4] == pytest.approx(1.0)
    assert lrs[5] == pytest.approx(1.0)


