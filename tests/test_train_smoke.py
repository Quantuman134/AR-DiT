def _make_val_cfg(batch_size_per_gpu: int, num_samples: int = 6):
    """Build a minimal cfg-like object for run_validation.

    ``run_validation`` reads ``cfg.validation.*``, ``cfg.model.*``,
    ``cfg.ema.decays``, and ``cfg.train.seed``.  We construct only these
    attributes rather than a full TrainConfig, both to keep the fixture
    tiny and to make the test's requirements explicit.  If ``run_validation``
    grows a new dependency on ``cfg.<something>``, this helper will fail
    loudly with an AttributeError — which is the desired signal.
    """
    from types import SimpleNamespace

    return SimpleNamespace(
        validation=SimpleNamespace(
            num_samples=num_samples,
            batch_size_per_gpu=batch_size_per_gpu,
            visual_log_count=num_samples,      # log all of them → tensor easy to compare
            guidance_scales=(1.0,),
            metrics=SimpleNamespace(fid=False, inception_score=False),
            sampler=SimpleNamespace(num_steps=2),
            fid_ref_stats=None,
            interval=1,
        ),
        model=SimpleNamespace(
            arch_name="DiT_S_2",
            input_size=8,        # tiny — 8×8 with patch=2 → 16 tokens
            in_channels=3,
            patch_size=2,
            num_classes=NUM_FIXTURE_CLASSES,
            class_dropout_prob=0.1,
        ),
        ema=SimpleNamespace(decays=()),        # no EMA shadows: only the "online" tag runs
        train=SimpleNamespace(seed=0),
    )


def test_run_validation_chunk_invariant(tmp_path: Path,
                                         monkeypatch: pytest.MonkeyPatch) -> None:
    """Chunking must not change the images or metrics.

    Reproducibility contract (see ``run_validation`` docstring):

    *   Chunked ``randn``/``randint`` draws from a re-seeded ``val_gen``
        produce the same values as a single-shot draw of the same total
        size, sliced.
    *   The Euler sampler is a pure function of (weights, noise, labels,
        num_steps, guidance_scale), so it must produce identical outputs
        chunk-by-chunk vs. single-shot.
    *   ``save_image`` receives the concatenation of the leading ``k``
        images — which, given the two points above, must be bit-identical
        across chunkings.

    We assert this by intercepting ``train.save_image`` (patched at the
    module level, since ``run_validation`` uses the imported reference)
    and comparing the two captured tensors with ``torch.equal`` — no
    tolerance, no round-trip through PNG.
    """
    import train as train_module
    from configs.schema import ModelConfig

    monkeypatch.setattr(torch.cuda, "is_available", lambda: False)

    # Same weights across both runs: build once, snapshot state_dict.
    model_cfg = ModelConfig(
        arch_name="DiT_S_2",
        input_size=8,
        in_channels=3,
        patch_size=2,
        num_classes=NUM_FIXTURE_CLASSES,
        class_dropout_prob=0.1,
    )
    torch.manual_seed(0)
    net_ref = train_module.build_model(model_cfg)
    ref_state = copy.deepcopy(net_ref.state_dict())

    device = torch.device("cpu")
    run_dir = tmp_path

    def _run_once(batch_size_per_gpu: int) -> torch.Tensor:
        # Fresh network with the same weights each time — run_validation
        # uses copy.deepcopy on the state_dict internally, so mutation is
        # not a concern, but we play it safe.
        net = train_module.build_model(model_cfg)
        net.load_state_dict(ref_state)

        cfg_like = _make_val_cfg(batch_size_per_gpu, num_samples=6)

        captured: dict[str, torch.Tensor] = {}
        real_save = train_module.save_image

        def _capture_save_image(tensor, path, *args, **kwargs):
            # `tensor` here is the make_grid output (a single 3xHxW image).
            # We want the pre-grid batch for a clean comparison — but the
            # grid is a deterministic function of the batch, so equality
            # of the batch ⟺ equality of the grid.  Comparing the grid is
            # therefore sufficient and lets us keep the patch surface small.
            captured["grid"] = tensor.detach().clone()
            return real_save(tensor, path, *args, **kwargs)

        monkeypatch.setattr(train_module, "save_image", _capture_save_image)
        try:
            train_module.run_validation(
                step=0,
                online_net=net,
                ema_dict={},                       # no EMA copies for this test
                cfg=cfg_like,
                device=device,
                run_dir=run_dir,
                fid_metric=None,
                is_metric=None,
                wandb_run=None,
            )
        finally:
            monkeypatch.setattr(train_module, "save_image", real_save)

        assert "grid" in captured, "save_image was never called by run_validation"
        return captured["grid"]

    # num_samples=6 with:
    #   bs=6 → one chunk    (baseline, mirrors pre-chunking behaviour)
    #   bs=2 → three chunks (exercises chunked accumulation)
    #   bs=4 → two chunks with a short tail of size 2
    grid_single = _run_once(batch_size_per_gpu=6)
    grid_three  = _run_once(batch_size_per_gpu=2)
    grid_two    = _run_once(batch_size_per_gpu=4)

    # Exact equality — the noise is bit-identical across chunkings, and
    # euler_sample is a pure fp32 function on CPU.
    assert torch.equal(grid_single, grid_three), (
        "chunked (bs=2) validation grid differs from single-shot (bs=6): "
        "the chunk loop is not preserving the noise stream or the sampler."
    )
    assert torch.equal(grid_single, grid_two), (
        "chunked (bs=4, tail=2) validation grid differs from single-shot "
        "(bs=6): the short-tail chunk is not being handled correctly."
    )

